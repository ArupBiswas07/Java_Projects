public class Variable {
    public static void main(){
        int a = 10;
        int b = 5;
        System.out.println(a);
        System.out.println(b);
        String name = "Arup Biswas";
        System.out.println(name);

        a = 50;  //here we changed the value of 'a'
        System.out.println(a);

        a = b;   //here the value of 'b' is put into 'a'
        System.out.println(a);
    }
}
